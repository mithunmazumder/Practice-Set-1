package Practice_Set_1;

public class PrimitiveData {
    public static void main(String[] args) {
        int a = 0;
        float b = 0.0f;
        long c = 0L;
        double d = 0.0d;
        byte e = 0;
        short f = 0;
        char g = '\u0000';
        boolean h = false;
        System.out.println("Integer default value: " + a);
        System.out.println("Float default value: " + b);
        System.out.println("long default value: " + c);
        System.out.println("Double default value: " + d);
        System.out.println("Byte default value: " + e);
        System.out.println("Short default value: " + f);
        System.out.println("Character default value: " + g);
        System.out.println("Boolean default value: " + h);
    }
}
